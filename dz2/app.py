from flask import Flask, render_template, request, redirect, url_for, make_response

app = Flask(__name__)
app.secret_key = b'your_secret_key'  # –ó–∞–º–µ–Ω–∏—Ç–µ 'your_secret_key' –Ω–∞ –≤–∞—à —É–Ω–∏–∫–∞–ª—å–Ω—ã–π —Å–µ–∫—Ä–µ—Ç–Ω—ã–π –∫–ª—é—á

@app.route('/')
def index():
    return render_template('index_task9.html')

@app.route('/welcome', methods=['POST'])
def welcome():
    if request.method == 'POST':
        # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–æ—Ä–º—ã
        name = request.form.get('name')
        email = request.form.get('email')

        # –°–æ–∑–¥–∞–µ–º cookie —Å –¥–∞–Ω–Ω—ã–º–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        response = make_response(redirect(url_for('greet')))
        response.set_cookie('user_data', f'{name},{email}')

        return response

@app.route('/greet')
def greet():
    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ cookie
    user_data_cookie = request.cookies.get('user_data')

    if user_data_cookie:
        # –†–∞–∑–¥–µ–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞ –∏–º—è –∏ —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω—É—é –ø–æ—á—Ç—É
        name, email = user_data_cookie.split(',')
        return render_template('welcome_task9.html', name=name, email=email)
    else:
        # –ï—Å–ª–∏ cookie –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –ø–µ—Ä–µ–Ω–∞–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É –≤–≤–æ–¥–∞
        return redirect(url_for('index'))

@app.route('/logout')
def logout():
    # –£–¥–∞–ª—è–µ–º cookie —Å –¥–∞–Ω–Ω—ã–º–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    response = make_response(redirect(url_for('index')))
    response.delete_cookie('user_data')
    return response

if __name__ == '__main__':
    app.run(debug=True)

